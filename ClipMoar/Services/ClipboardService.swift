import Cocoa
import CryptoKit
import ImageIO

protocol ClipboardLogger {
    func log(_ message: StaticString, _ args: CVarArg...)
}

struct SystemClipboardLogger: ClipboardLogger {
    func log(_ message: StaticString, _ args: CVarArg...) {
        withVaList(args) { NSLogv(String(describing: message), $0) }
    }
}

protocol PasteboardReadable {
    var changeCount: Int { get }
    var frontmostBundleId: String? { get }
    func stringValue() -> String?
    func imageData() -> Data?
    func isEmpty() -> Bool
    func fileURLs() -> [URL]?
    func hasMarkerType() -> Bool
    func hasIgnoredSystemType() -> Bool
}

final class NSPasteboardGateway: PasteboardReadable {
    private static let transientType = NSPasteboard.PasteboardType("org.nspasteboard.TransientType")
    private static let autoGeneratedType = NSPasteboard.PasteboardType("org.nspasteboard.AutoGeneratedType")

    var changeCount: Int {
        NSPasteboard.general.changeCount
    }

    var frontmostBundleId: String? {
        NSWorkspace.shared.frontmostApplication?.bundleIdentifier
    }

    func stringValue() -> String? {
        NSPasteboard.general.string(forType: .string)
    }

    func imageData() -> Data? {
        NSPasteboard.general.data(forType: .png)
    }

    func fileURLs() -> [URL]? {
        guard let urls = NSPasteboard.general.readObjects(forClasses: [NSURL.self], options: [
            .urlReadingFileURLsOnly: true,
        ]) as? [URL], !urls.isEmpty else { return nil }
        return urls
    }

    func isEmpty() -> Bool {
        guard let types = NSPasteboard.general.types, !types.isEmpty else { return true }
        return !types.contains(.string) && !types.contains(.png) && !types.contains(.fileURL)
    }

    func hasMarkerType() -> Bool {
        NSPasteboard.general.data(forType: ClipboardActionService.markerType) != nil
    }

    func hasIgnoredSystemType() -> Bool {
        let pb = NSPasteboard.general
        return pb.data(forType: Self.transientType) != nil
            || pb.data(forType: Self.autoGeneratedType) != nil
    }
}

enum ContentFingerprint {
    static func hash(text: String) -> String {
        guard let data = text.data(using: .utf8) else { return "" }
        return SHA256.hash(data: data).compactMap { String(format: "%02x", $0) }.joined()
    }

    static func hash(data: Data) -> String {
        SHA256.hash(data: data).compactMap { String(format: "%02x", $0) }.joined()
    }
}

private enum ClipboardSnapshotContent {
    case empty
    case text(String)
    case image(Data)
    case files([URL])
    case unsupported
}

private struct ClipboardSnapshot {
    let changeCount: Int
    let sourceAppBundleId: String?
    let hasMarkerType: Bool
    let hasIgnoredSystemType: Bool
    let content: ClipboardSnapshotContent
}

final class ClipboardService {
    /// These sources become noisy only when the app runs under the Xcode test host.
    private static let testHarnessIgnoredBundleIds: Set<String> = [
        "com.apple.dt.Xcode",
        "com.apple.dt.XcodePreviews",
        "com.apple.iphonesimulator",
        "com.apple.CoreSimulator.SimulatorTrampoline",
    ]

    private let repository: ClipboardRepository
    private let settings: SettingsStore
    private let pasteboard: PasteboardReadable
    private let ruleEngine: ClipboardRuleEngine
    private let logger: ClipboardLogger
    private let isRunningUnderTestHarness: () -> Bool
    private var timer: Timer?
    private var lastChangeCount: Int = 0
    private let monitorInterval: TimeInterval
    private let maintenanceInterval: Int
    private var lastInsertedUUID: UUID?
    private var pendingMaintenancePasses = 0
    var onStatEvent: ((StatEventKind) -> Void)?

    init(
        repository: ClipboardRepository = CoreDataClipboardRepository(),
        settings: SettingsStore = UserDefaultsSettingsStore(),
        pasteboard: PasteboardReadable = NSPasteboardGateway(),
        ruleEngine: ClipboardRuleEngine = ClipboardRuleEngine(),
        monitorInterval: TimeInterval = 0.5,
        maintenanceInterval: Int = 20,
        logger: ClipboardLogger = SystemClipboardLogger(),
        isRunningUnderTestHarness: @escaping () -> Bool = ClipboardService.defaultTestHarnessDetector
    ) {
        self.repository = repository
        self.settings = settings
        self.pasteboard = pasteboard
        self.ruleEngine = ruleEngine
        self.monitorInterval = monitorInterval
        self.maintenanceInterval = max(1, maintenanceInterval)
        self.logger = logger
        self.isRunningUnderTestHarness = isRunningUnderTestHarness
    }

    func startMonitoring() {
        guard timer == nil else { return }
        lastChangeCount = pasteboard.changeCount
        timer = Timer.scheduledTimer(withTimeInterval: monitorInterval, repeats: true) { [weak self] _ in
            self?.checkForChanges()
        }
    }

    func stopMonitoring() {
        timer?.invalidate()
        timer = nil
    }

    func checkForChanges() {
        let newCount = pasteboard.changeCount
        guard newCount != lastChangeCount else { return }
        autoreleasepool {
            processChange(newCount: newCount)
        }
    }

    private func processChange(newCount: Int) {
        let gap = newCount - lastChangeCount
        let snapshot = readSnapshot(changeCount: newCount)
        lastChangeCount = snapshot.changeCount

        if snapshot.hasMarkerType {
            logger.log("[ClipMoar] skip: own marker")
            return
        }
        if snapshot.hasIgnoredSystemType {
            logger.log("[ClipMoar] skip: ignored system type (transient/auto-generated)")
            return
        }
        if shouldIgnoreSnapshot(from: snapshot.sourceAppBundleId) {
            return
        }

        switch snapshot.content {
        case .empty:
            logger.log("[ClipMoar] skip: empty pasteboard")
            if let uuid = lastInsertedUUID {
                repository.removeItem(uuid: uuid)
                lastInsertedUUID = nil
            }
            return
        case let .files(urls):
            let sourceAppBundleId = snapshot.sourceAppBundleId
            logger.log("[ClipMoar] new copy from: %@ storeText=%d storeImages=%d", sourceAppBundleId ?? "unknown", settings.storeText ? 1 : 0, settings.storeImages ? 1 : 0)

            if urls.count == 1, let imageData = imageDataFromFileURL(urls[0]) {
                guard settings.storeImages else {
                    logger.log("[ClipMoar] skip: image file but storeImages=false")
                    return
                }
                let fingerprint = ContentFingerprint.hash(data: imageData)
                guard !handleDuplicate(fingerprint: fingerprint, gap: gap) else {
                    logger.log("[ClipMoar] skip: duplicate image file")
                    return
                }
                var finalData = compressImageIfNeeded(imageData)
                finalData = processImageIfNeeded(finalData)
                lastInsertedUUID = repository.insertImage(finalData, sourceAppBundleId: sourceAppBundleId, fingerprint: fingerprint)
                repository.releaseMemory()
                logger.log("[ClipMoar] inserted image from file: %@ (%d bytes)", urls[0].lastPathComponent, finalData.count)
                onStatEvent?(.copy)
                scheduleMaintenance()
                return
            }

            let paths = urls.map { $0.path }.joined(separator: "\n")
            let fingerprint = ContentFingerprint.hash(text: "file:" + paths)
            guard !handleDuplicate(fingerprint: fingerprint, gap: gap) else {
                logger.log("[ClipMoar] skip: duplicate file")
                return
            }

            lastInsertedUUID = repository.insertFile(paths, sourceAppBundleId: sourceAppBundleId, fingerprint: fingerprint)
            logger.log("[ClipMoar] inserted file: %@", paths)
            onStatEvent?(.copy)
            scheduleMaintenance()
        case let .text(string):
            guard settings.storeText, !string.isEmpty else {
                logger.log("[ClipMoar] skip: no matching content (storeText=%d hasString=%d hasImage=%d)", settings.storeText ? 1 : 0, 1, 0)
                return
            }
            let sourceAppBundleId = snapshot.sourceAppBundleId
            logger.log("[ClipMoar] new copy from: %@ storeText=%d storeImages=%d", sourceAppBundleId ?? "unknown", settings.storeText ? 1 : 0, settings.storeImages ? 1 : 0)
            let result = ruleEngine.apply(to: string, sourceAppBundleId: sourceAppBundleId)

            if result.text != string {
                logger.log("[ClipMoar] text transformed, writing back to clipboard")
                writeTransformedText(result.text)
            }

            let fingerprint = ContentFingerprint.hash(text: result.text)
            guard !handleDuplicate(fingerprint: fingerprint, gap: gap) else {
                logger.log("[ClipMoar] skip: duplicate text (transform still applied)")
                return
            }

            let appliedRule = result.appliedRules.isEmpty ? nil : result.appliedRules.joined(separator: ", ")
            lastInsertedUUID = repository.insertText(result.text, sourceAppBundleId: sourceAppBundleId, fingerprint: fingerprint, appliedRule: appliedRule)
            logger.log("[ClipMoar] inserted text (rule: %@): %@", appliedRule ?? "none", String(result.text.prefix(80)))
            onStatEvent?(.copy)
            scheduleMaintenance()
        case let .image(imageData):
            guard settings.storeImages else {
                logger.log("[ClipMoar] skip: no matching content (storeText=%d hasString=%d hasImage=%d)", settings.storeText ? 1 : 0, 0, 1)
                return
            }
            let sourceAppBundleId = snapshot.sourceAppBundleId
            logger.log("[ClipMoar] new copy from: %@ storeText=%d storeImages=%d", sourceAppBundleId ?? "unknown", settings.storeText ? 1 : 0, settings.storeImages ? 1 : 0)
            let fingerprint = ContentFingerprint.hash(data: imageData)
            guard !handleDuplicate(fingerprint: fingerprint, gap: gap) else {
                logger.log("[ClipMoar] skip: duplicate image")
                return
            }

            var finalData = compressImageIfNeeded(imageData)
            finalData = processImageIfNeeded(finalData)

            if finalData != imageData {
                let pb = NSPasteboard.general
                pb.clearContents()
                pb.setData(finalData, forType: .png)
                pb.setData(Data(), forType: ClipboardActionService.markerType)
            }

            lastInsertedUUID = repository.insertImage(finalData, sourceAppBundleId: sourceAppBundleId, fingerprint: fingerprint)
            repository.releaseMemory()
            logger.log("[ClipMoar] inserted image: %d bytes (original: %d)", finalData.count, imageData.count)
            onStatEvent?(.copy)
            scheduleMaintenance()
        case .unsupported:
            logger.log("[ClipMoar] skip: no matching content (storeText=%d hasString=%d hasImage=%d)", settings.storeText ? 1 : 0, 0, 0)
        }
    }

    private func readSnapshot(changeCount: Int) -> ClipboardSnapshot {
        let hasMarkerType = pasteboard.hasMarkerType()
        let hasIgnoredSystemType = pasteboard.hasIgnoredSystemType()

        let content: ClipboardSnapshotContent
        if pasteboard.isEmpty() {
            content = .empty
        } else if let urls = pasteboard.fileURLs() {
            content = .files(urls)
        } else if let text = pasteboard.stringValue() {
            content = .text(text)
        } else if let data = pasteboard.imageData(), data.count > 0 {
            content = .image(data)
        } else {
            content = .unsupported
        }

        return ClipboardSnapshot(
            changeCount: changeCount,
            sourceAppBundleId: pasteboard.frontmostBundleId,
            hasMarkerType: hasMarkerType,
            hasIgnoredSystemType: hasIgnoredSystemType,
            content: content
        )
    }

    private func handleDuplicate(fingerprint: String, gap: Int) -> Bool {
        guard repository.isDuplicate(fingerprint: fingerprint) else { return false }
        if gap > 1, let uuid = lastInsertedUUID {
            repository.removeItem(uuid: uuid)
            lastInsertedUUID = nil
        }
        return true
    }

    private func writeTransformedText(_ text: String) {
        let pb = NSPasteboard.general
        pb.clearContents()
        pb.setString(text, forType: .string)
        pb.setData(Data(), forType: ClipboardActionService.markerType)
        lastChangeCount = pb.changeCount
    }

    private func scheduleMaintenance() {
        pendingMaintenancePasses += 1
        guard pendingMaintenancePasses >= maintenanceInterval else { return }
        pendingMaintenancePasses = 0
        repository.trimHistory(maxSize: max(settings.maxHistorySize, 1))
        cleanupOldItems()
    }

    private func cleanupOldItems() {
        repository.removeOlderThan(hours: settings.textRetentionHours, contentType: ClipboardItemType.text.rawValue)
        repository.removeOlderThan(hours: settings.textRetentionHours, contentType: ClipboardItemType.file.rawValue)
        repository.removeOlderThan(hours: settings.imageRetentionHours, contentType: ClipboardItemType.image.rawValue)
    }

    private static let imageExtensions: Set<String> = ["png", "jpg", "jpeg", "gif", "tiff", "tif", "bmp", "webp", "heic", "heif"]

    private func imageDataFromFileURL(_ url: URL) -> Data? {
        guard Self.imageExtensions.contains(url.pathExtension.lowercased()),
              FileManager.default.isReadableFile(atPath: url.path),
              let data = try? Data(contentsOf: url),
              NSImage(data: data) != nil
        else { return nil }
        return data
    }

    private func processImageIfNeeded(_ data: Data) -> Data {
        let s = settings
        guard s.imageConvertToPNG || s.imageConvertToJPEG
            || s.imageStripMetadata || s.imageAutoEnhance || s.imageGrayscale
            || s.imageAutoRotate || s.imageTrimWhitespace || s.imageSharpen || s.imageReduceNoise
        else { return data }
        return ImageProcessor.process(data, settings: settings)
    }

    private func compressImageIfNeeded(_ data: Data) -> Data {
        guard settings.compressImages else { return data }
        guard let source = CGImageSourceCreateWithData(data as CFData, nil),
              let props = CGImageSourceCopyPropertiesAtIndex(source, 0, nil) as? [CFString: Any],
              let origW = props[kCGImagePropertyPixelWidth] as? CGFloat,
              let origH = props[kCGImagePropertyPixelHeight] as? CGFloat
        else { return data }

        var width = origW, height = origH
        let maxW = CGFloat(settings.imageMaxWidth)
        let maxH = CGFloat(settings.imageMaxHeight)
        if maxW > 0, width > maxW { let s = maxW / width; height *= s; width = maxW }
        if maxH > 0, height > maxH { let s = maxH / height; width *= s; height = maxH }

        let thumbOptions: [CFString: Any] = [
            kCGImageSourceThumbnailMaxPixelSize: max(width, height),
            kCGImageSourceCreateThumbnailFromImageAlways: true,
            kCGImageSourceCreateThumbnailWithTransform: true,
        ]
        guard let cgImage = CGImageSourceCreateThumbnailAtIndex(source, 0, thumbOptions as CFDictionary)
        else { return data }

        let mutableData = NSMutableData()
        guard let dest = CGImageDestinationCreateWithData(
            mutableData, "public.jpeg" as CFString, 1, nil
        ) else { return data }
        let quality = CGFloat(settings.imageQuality) / 100.0
        CGImageDestinationAddImage(dest, cgImage, [
            kCGImageDestinationLossyCompressionQuality: quality,
        ] as CFDictionary)
        guard CGImageDestinationFinalize(dest) else { return data }
        return mutableData as Data
    }

    private func shouldIgnoreSnapshot(from sourceAppBundleId: String?) -> Bool {
        if let sourceAppBundleId, settings.ignoredAppBundleIds.contains(sourceAppBundleId) {
            logger.log("[ClipMoar] skip: ignored app %@", sourceAppBundleId)
            return true
        }
        guard isRunningUnderTestHarness(), let sourceAppBundleId else { return false }
        return Self.testHarnessIgnoredBundleIds.contains(sourceAppBundleId)
    }

    private static func defaultTestHarnessDetector() -> Bool {
        let environment = ProcessInfo.processInfo.environment
        return environment["XCTestConfigurationFilePath"] != nil
            || environment["XCTestSessionIdentifier"] != nil
            || environment["XCInjectBundleInto"] != nil
            || environment["XCTestBundlePath"] != nil
    }
}
